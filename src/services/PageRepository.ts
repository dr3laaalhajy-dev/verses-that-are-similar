/**
 * returns the page number for a given surah and verse.
 */
export const getPageNumber = (surahId: number, verseId: number): number => {
  // Find the last boundary that is before or equal to the current surah and verse
  for (let i = pageBoundaries.length - 1; i >= 0; i--) {
    const b = pageBoundaries[i];
    if (surahId > b.startSurah || (surahId === b.startSurah && verseId >= b.startVerse)) {
      return Math.min(b.page, 604);
    }
  }
  return 1;
};
